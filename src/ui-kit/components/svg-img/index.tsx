import React, { FC, useContext, useState, MouseEvent } from 'react';
import classnames from 'classnames';
import { BaseProps } from '../util/type';
import { getPath, getViewBox, PathOptions } from './svg-dict';
import { SvgIconEnum } from './type';
import './index.css';
import { themeContext } from 'agora-common-libs';

export type SvgImgProps = BaseProps & {
  type: SvgIconEnum;
  colors?: Partial<PathOptions>;
  size?: number | string;
  onClick?: (e: MouseEvent) => void;
  onMouseDown?: (e: MouseEvent) => void;
  onMouseUp?: (e: MouseEvent) => void;
};
export type SvgImgMobileProps = BaseProps & {
  type: SvgIconEnum;
  colors?: Partial<PathOptions>;
  size?: number;
  onClick?: (e: MouseEvent) => void;
  onMouseDown?: (e: MouseEvent) => void;
  onMouseUp?: (e: MouseEvent) => void;
};
export const SvgImg: FC<SvgImgProps> = ({
  type,
  size = 24,
  onClick,
  className,
  style,
  colors,
  onMouseDown,
  onMouseUp,
}) => {
  const cls = classnames({
    [`${className}`]: !!className,
  });
  const { iconPrimary, iconSecondary } = useContext(themeContext);

  const viewBox = getViewBox(type);
  const path = getPath(type, {
    ...colors,
    iconPrimary: colors?.iconPrimary ?? iconPrimary,
    iconSecondary: colors?.iconSecondary ?? iconSecondary,
  });

  return (
    <svg
      className={cls}
      width={size}
      height={size}
      viewBox={viewBox}
      xmlns="http://www.w3.org/2000/svg"
      xmlnsXlink="http://www.w3.org/1999/xlink"
      onClick={onClick}
      onMouseUp={onMouseUp}
      onMouseDown={onMouseDown}
      style={style}
      data-label={type}>
      {path}
    </svg>
  );
};
export const SvgImgMobile: FC<SvgImgMobileProps & { landscape: boolean; forceLandscape: boolean }> =
  (props) => {
    const { landscape, forceLandscape } = props;
    const basicWidth =
      (forceLandscape || landscape) && window.matchMedia('(orientation: landscape)').matches
        ? 812
        : 375;
    const basicWidthRatio = (((props.size || 24) / basicWidth) * 100).toFixed(5) + 'vw';

    return <SvgImg {...props} size={basicWidthRatio}></SvgImg>;
  };
export type SvgIconProps = BaseProps & {
  type: SvgIconEnum;
  colors?: Partial<PathOptions>;
  hoverType: SvgIconEnum;
  hoverColors?: Partial<PathOptions>;
  size?: number;
  onClick?: any;
  canHover?: boolean;
};

// Icon that has hoverable surroundings
export const SvgIcon: FC<SvgIconProps> = ({
  type,
  hoverType,
  size,
  onClick,
  className,
  style,
  colors,
  hoverColors,
  canHover = true,
}) => {
  const [hovering, setHovering] = useState<boolean>(false);

  const t = hovering && hoverType ? hoverType : type;
  const c = hovering && hoverColors ? hoverColors : colors;

  return (
    <div
      className={canHover ? 'can-hover' : ''}
      style={{ display: 'flex' }}
      onMouseEnter={() => setHovering(true)}
      onMouseLeave={() => setHovering(false)}>
      <SvgImg
        type={t}
        colors={c}
        size={size}
        onClick={onClick}
        className={className}
        style={style}
      />
    </div>
  );
};

export { SvgIconEnum } from './type';
